#include <stddef.h>

void	*ft_memset(void *b, int c, size_t len)
{
	void	*ptr;

	ptr = b;
	while (len--)
	{
		*(char *)b = (char)c;
		b = (char *)b + 1;
	}
	return (ptr);
}
